export interface SearchOptions {
  pattern?: string;
  extensions?: string;
  minSize?: number;
  maxSize?: number;
  afterDate?: string;
  beforeDate?: string;
  contentQuery?: string;
  recursive?: boolean;
  sortBy?: string;
  caseSensitive?: boolean;
  maxResults?: number;
  showDetails?: boolean;
}

// BUG-FIX: Made searchDirectory async to support fs.promises operations
export async function searchDirectory(
  rootDir: string,
  options: SearchOptions = {}
): Promise<string> {
  const root = path.resolve(rootDir);

  if (!fs.existsSync(root)) {
    return `Error: Directory '${rootDir}' does not exist.`;
  }

  if (!fs.statSync(root).isDirectory()) {
    return `Error: '${rootDir}' is not a directory.`;
  }

  const allResults: SearchResult[] = [];

  try {
    // Name pattern search
    if (options.pattern) {
      // BUG-FIX: Await async search functions
      const results = await searchByName(root, options.pattern, options.recursive !== false, options.caseSensitive || false);
      allResults.push(...results);
    }

    // Extension search
    if (options.extensions) {
      const exts = options.extensions.split(',').map(e => e.trim().replace(/^\./, ''));
      // BUG-FIX: Await async search functions
      const results = await searchByExtension(root, exts, options.recursive !== false);
      allResults.push(...results);
    }

    // Size search
    if (options.minSize !== undefined || options.maxSize !== undefined) {
      // BUG-FIX: Await async search functions
      const results = await searchBySize(root, options.minSize, options.maxSize, options.recursive !== false);
      allResults.push(...results);
    }

    // Date search
    if (options.afterDate || options.beforeDate) {
      // BUG-FIX: Await async search functions
      const results = await searchByDate(root, options.afterDate, options.beforeDate, options.recursive !== false);
      allResults.push(...results);
    }

    // Content search
    if (options.contentQuery) {
      // BUG-FIX: Await async search functions
      const results = await searchContent(root, options.contentQuery, options.recursive !== false, options.pattern, options.caseSensitive || false, options.maxResults || 100);
      allResults.push(...results);
    }

    // Deduplicate by path
    const seen = new Set<string>();
    const unique = allResults.filter(r => {
      if (seen.has(r.path)) return false;
      seen.add(r.path);
      return true;
    });

    // Sort
    let sorted = sortResults(unique, options.sortBy || 'name');

    // Limit
    const maxResults = options.maxResults || 200;
    sorted = sorted.slice(0, maxResults);

    // Format
    const output = formatResults(sorted, options.showDetails !== false);
    return output;

  } catch (e) {
    return `Error searching directory: ${e instanceof Error ? e.message : String(e)}`;
  }
}

interface SearchResult {
  path: string;
  name: string;
  isDir: boolean;
  size?: number;
  modified?: string;
  fileType?: string;
}

function formatSize(sizeBytes: number): string {
  const units = ['B', 'KB', 'MB', 'GB', 'TB'];
  let size = sizeBytes;
  for (let i = 0; i < units.length; i++) {
    if (size < 1024 || i === units.length - 1) {
      return `${size.toFixed(1)} ${units[i]}`;
    }
    size /= 1024;
  }
  return `${size.toFixed(1)} PB`;
}

function formatDate(timestamp: number): string {
  return new Date(timestamp * 1000).toLocaleString('en-US', {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit'
  });
}

// BUG-FIX: Made async to use fs.promises.readdir
async function searchByName(rootDir: string, pattern: string, recursive: boolean, caseSensitive: boolean): Promise<SearchResult[]> {
  const results: SearchResult[] = [];
  const searchDir = path.resolve(rootDir);

  async function walk(dir: string) {
    const entries = await fs.promises.readdir(dir, { withFileTypes: true });

    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);

      if (fnmatchMatch(entry.name, pattern, caseSensitive)) {
        const stat = await fs.promises.stat(fullPath);
        results.push({
          path: fullPath,
          name: entry.name,
          isDir: entry.isDirectory(),
          size: entry.isFile() ? stat.size : undefined,
          modified: entry.isFile() ? formatDate(stat.mtimeMs / 1000) : undefined,
          fileType: entry.isFile() ? (path.extname(entry.name).slice(1).toUpperCase() || 'FILE') : 'DIR'
        });
      }

      if (entry.isDirectory() && recursive) {
        await walk(fullPath);
      }
    }
  }

  await walk(searchDir);
  return results;
}

function fnmatchMatch(name: string, pattern: string, caseSensitive: boolean): boolean {
  const n = caseSensitive ? name : name.toLowerCase();
  const p = caseSensitive ? pattern : pattern.toLowerCase();

  // Simple glob matching
  const regex = new RegExp('^' + p.replace(/[.+^${}()|[\]\\]/g, '\\$&').replace(/\*/g, '.*').replace(/\?/g, '.') + '$');
  return regex.test(n);
}

// BUG-FIX: Made async to use fs.promises.readdir
async function searchByExtension(rootDir: string, extensions: string[], recursive: boolean): Promise<SearchResult[]> {
  const results: SearchResult[] = [];
  const extSet = new Set(extensions.map(e => e.toLowerCase()));
  const searchDir = path.resolve(rootDir);

  async function walk(dir: string) {
    const entries = await fs.promises.readdir(dir, { withFileTypes: true });

    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      const stat = await fs.promises.stat(fullPath);

      if (entry.isDirectory() && recursive) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        const fileExt = path.extname(entry.name).slice(1).toLowerCase();
        if (extSet.has(fileExt)) {
          results.push({
            path: fullPath,
            name: entry.name,
            isDir: false,
            size: stat.size,
            modified: formatDate(stat.mtimeMs / 1000),
            fileType: fileExt.toUpperCase()
          });
        }
      }
    }
  }

  await walk(searchDir);
  return results;
}

// BUG-FIX: Made async to use fs.promises.readdir
async function searchBySize(rootDir: string, minSize?: number, maxSize?: number, recursive: boolean = true): Promise<SearchResult[]> {
  const results: SearchResult[] = [];
  const searchDir = path.resolve(rootDir);

  async function walk(dir: string) {
    const entries = await fs.promises.readdir(dir, { withFileTypes: true });

    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      const stat = await fs.promises.stat(fullPath);

      if (entry.isDirectory() && recursive) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        if ((minSize === undefined || stat.size >= minSize) &&
            (maxSize === undefined || stat.size <= maxSize)) {
          results.push({
            path: fullPath,
            name: entry.name,
            isDir: false,
            size: stat.size,
            modified: formatDate(stat.mtimeMs / 1000),
            fileType: path.extname(entry.name).slice(1).toUpperCase() || 'FILE'
          });
        }
      }
    }
  }

  await walk(searchDir);
  return results;
}

