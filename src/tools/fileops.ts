async function createDocx(outputPath: string, content: string, options: CreateOptions): Promise<string> {
  const title = options.title || 'Document';
  const paragraphs: string[] = options.paragraphs
    ? options.paragraphs.split('|').map(p => p.trim())
    : content ? content.split('\n') : [];

  const docChildren: Paragraph[] = [
    new Paragraph({
      children: [new TextRun({ text: title, bold: true, size: 48 })],
      heading: HeadingLevel.HEADING_1,
      alignment: AlignmentType.CENTER,
      spacing: { after: 400 }
    })
  ];

  if (paragraphs.length > 0) {
    paragraphs.forEach(para => {
      if (para.trim()) {
        docChildren.push(
          new Paragraph({
            children: [new TextRun(para)],
            spacing: { after: 200 }
          })
        );
      }
    });
  }

  if (content && !options.paragraphs) {
    content.split('\n').forEach(line => {
      if (line.trim()) {
        docChildren.push(
          new Paragraph({
            children: [new TextRun(line)],
            spacing: { after: 200 }
          })
        );
      }
    });
  }

  const doc = new Document({
    sections: [{
      properties: {},
      children: docChildren,
    }],
  });

  const buffer = await Packer.toBuffer(doc);
  fs.writeFileSync(outputPath, buffer);
  return `Created DOCX file: ${outputPath} (${buffer.length} bytes)`;
}

async function createPdf(outputPath: string, content: string, options: CreateOptions): Promise<string> {
  const doc = new PDFDocument({ autoFirstPage: false });
  const stream = fs.createWriteStream(outputPath);
  doc.pipe(stream);

  // BUG-30 FIX: Add a page before writing content (autoFirstPage: false means no page is created automatically)
  doc.addPage();

  const title = options.title || 'Document';

  // Title
  doc.fontSize(28).text(title, {
    align: 'center',
    underline: true
  });

  doc.moveDown(1);

  // Content
  if (content) {
    const lines = content.split('\n');
    // BUG-57 FIX: Add max-lines limit to prevent memory issues with large content
    const maxLines = 10000;
    const limitedLines = lines.slice(0, maxLines);
    limitedLines.forEach(line => {
      doc.fontSize(12).text(line, {
        align: 'left',
        indent: 20
      });
      doc.moveDown(0.3);
    });
    if (lines.length > maxLines) {
      doc.fontSize(10).fillColor('#999').text(`... [truncated, ${lines.length - maxLines} lines omitted]`, {
        align: 'left',
        indent: 20
      }).fillColor('#000000');
    }
  } else {
    doc.fontSize(12).text('This document was created by the Universal Toolkit.', {
      align: 'left',
      indent: 20
    });
  }

  doc.end();

  await new Promise<void>((resolve, reject) => {
    stream.on('finish', () => resolve());
    stream.on('error', (err) => reject(err));
  });

  return `Created PDF file: ${outputPath}`;
}

// ===================== FILE READING =====================

export async function readFile(filepath: string): Promise<string> {
  const filePath = path.resolve(filepath);

  if (!fs.existsSync(filePath)) {
    return `Error: File not found: ${filepath}`;
  }

  const ext = path.extname(filePath).toLowerCase();
  const stat = fs.statSync(filePath);

  try {
    switch (ext) {
      case '.txt':
        return readTxt(filePath, stat);

      case '.md':
        return readMarkdown(filePath, stat);

      case '.json':
        return readJson(filePath, stat);

      case '.csv':
        return readCsv(filePath, stat);

      case '.html':
      case '.htm':
        return await readHtml(filePath, stat);

      case '.docx':
        return readDocx(filePath, stat);

      case '.pdf':
        return readPdf(filePath, stat);

      default:
        return `Unsupported file type: ${ext}. Supported: .txt, .md, .json, .csv, .html, .docx, .pdf`;
    }
  } catch (e) {
    return `Error reading file: ${e instanceof Error ? e.message : String(e)}`;
  }
}

function readTxt(filePath: string, stat: fs.Stats): string {
  const content = fs.readFileSync(filePath, 'utf8');
  return `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n${content}`;
}

function readMarkdown(filePath: string, stat: fs.Stats): string {
  const content = fs.readFileSync(filePath, 'utf8');
  return `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n${content}`;
}

function readJson(filePath: string, stat: fs.Stats): string {
  const content = fs.readFileSync(filePath, 'utf8');
  let data: any;
  try {
    data = JSON.parse(content);
  } catch (e) {
    return `Error: Invalid JSON in ${filePath}: ${e instanceof Error ? e.message : String(e)}`;
  }
  return `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n${JSON.stringify(data, null, 2)}`;
}

function readCsv(filePath: string, stat: fs.Stats): string {
  const content = fs.readFileSync(filePath, 'utf8');
  // BUG-42 FIX: Handle newlines in quoted fields by parsing CSV properly
  // The csv-stringify library has a corresponding parse function we can use
  let rows: string[][];
  try {
    // Use csv-stringify's parse (it exports parse as well)
    // For now, implement a simple state-machine parser for quoted fields
    rows = parseCsvContent(content);
  } catch (e) {
    // Fallback to simple split if parsing fails
    rows = content.split('\n').map(row => parseCsvRow(row)).filter(row => row.length > 0 && (row.length > 1 || row[0].trim() !== ''));
  }

  if (rows.length === 0) {
    return `--- ${filePath} (empty file) ---`;
  }

  const maxRows = Math.min(rows.length, 51);
  const header = rows[0];
  const colWidths = header.map((_: string, colIdx: number) =>
    Math.max(
      header[colIdx].length,
      ...rows.slice(1, Math.min(rows.length, 51)).map(row => row[colIdx]?.length || 0)
    )
  );

  let output = `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n`;
  output += `Rows: ${rows.length - 1} | Columns: ${header.length}\n`;
  output += '='.repeat(70) + '\n\n';

  // Header
  const headerStr = header.map((h: string, i: number) => h.padEnd(Math.min(colWidths[i], 30))).join(' | ');
  output += headerStr + '\n';
  output += '-'.repeat(headerStr.length) + '\n';

  // Data rows
  for (let i = 1; i < maxRows; i++) {
    const row = rows[i];
    const rowStr = header.map((_: string, j: number) => (row[j] || '').padEnd(Math.min(colWidths[j], 30))).join(' | ');
    output += `${String(i).padStart(4)} | ${rowStr}\n`;
  }

  if (rows.length > maxRows) {
    output += `\n... and ${rows.length - maxRows} more rows\n`;
  }

  return output;
}

async function readHtml(filePath: string, stat: fs.Stats): Promise<string> {
  const html = fs.readFileSync(filePath, 'utf8');

  try {
    const { default: cheerio } = await import('cheerio');
    const $ = cheerio.load(html);
    const title = $('title').text() || 'No title';
    const links = $('a[href]');

    let output = `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n`;
    output += `Title: ${title}\n`;
    output += `Links: ${links.length}\n`;
    output += '='.repeat(70) + '\n\n';

    // Extract paragraphs
    $('p, h1, h2, h3, h4, h5, h6, li').each((_: any, el: any) => {
      const text = $(el).text().trim();
      if (text) {
        output += text + '\n';
      }
    });

    // Show links
    if (links.length > 0) {
      output += '\n' + '='.repeat(70) + '\n';
      output += 'LINKS:\n';
      links.slice(0, 20).each((_: any, el: any) => {
        const linkText = $(el).text().trim().substring(0, 40);
        const href = $(el).attr('href') || '';
        output += `  ${linkText.padEnd(40)} -> ${href}\n`;
      });
      if (links.length > 20) {
        output += `  ... and ${links.length - 20} more\n`;
      }
    }

    return output;
  } catch (e) {
    return `Error reading HTML: ${e instanceof Error ? e.message : String(e)}`;
  }
}

async function readDocx(filePath: string, stat: fs.Stats): Promise<string> {
  try {
    const { default: JSZip } = await import('jszip');

    const buffer = fs.readFileSync(filePath);
    const zip = await JSZip.loadAsync(buffer);
    const documentXml = await zip.file('word/document.xml')?.async('string');

    if (!documentXml) {
      return `Error: Invalid DOCX file`;
    }

    // Simple text extraction from XML
    const textMatches = documentXml.match(/<w:t[^>]*>([^<]*)<\/w:t>/g) || [];
    const texts = textMatches.map((m: string) => {
      const text = m.match(/>([^<]*)<\/w:t>/);
      return text ? text[1] : '';
    });

    let output = `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n`;
    output += `Paragraphs: ${texts.filter((t: string) => t.trim()).length}\n`;
    output += '='.repeat(70) + '\n\n';

    texts.forEach((text: string, i: number) => {
      if (text.trim()) {
        output += `[${i + 1}] ${text.trim()}\n`;
      }
    });

    return output;
  } catch (e) {
    return `Error reading DOCX: ${e instanceof Error ? e.message : String(e)}. Use a dedicated tool for full DOCX support.`;
  }
}

async function readPdf(filePath: string, stat: fs.Stats): Promise<string> {
  try {
    const buffer = fs.readFileSync(filePath);
    const data = await pdfParse(buffer);

    let output = `--- ${filePath} (${stat.size.toLocaleString()} bytes) ---\n`;
    output += `Pages: ${data.numpages}\n`;
    output += '='.repeat(70) + '\n\n';

    if (data.text && data.text.trim()) {
      output += data.text;
    } else {
      output += '[No extractable text]';
    }

    return output;
  } catch (e) {
    return `Error reading PDF: ${e instanceof Error ? e.message : String(e)}`;
  }
}

