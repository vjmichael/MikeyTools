// BUG-FIX: Made async to use fs.promises.readdir
async function searchContent(rootDir: string, query: string, recursive: boolean = true, filePattern?: string, caseSensitive: boolean = false, maxResults: number = 100): Promise<SearchResult[]> {
  const results: SearchResult[] = [];
  const searchDir = path.resolve(rootDir);

  const flags = caseSensitive ? '' : 'i';
  // BUG-15 FIX: Limit query length and escape special chars to prevent ReDoS
  const maxQueryLen = 100;
  if (query.length > maxQueryLen) {
    throw new Error(`Content search query too long (max ${maxQueryLen} chars)`);
  }
  const escapedQuery = query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  let regex: RegExp;
  try {
    regex = new RegExp(escapedQuery, flags);
  } catch {
    regex = new RegExp(escapedQuery, flags);
  }

  async function walk(dir: string) {
    if (results.length >= maxResults) return;

    const entries = await fs.promises.readdir(dir, { withFileTypes: true });

    for (const entry of entries) {
      if (results.length >= maxResults) return;

      const fullPath = path.join(dir, entry.name);

      if (entry.isDirectory() && recursive) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        if (filePattern && !fnmatchMatch(entry.name, filePattern, caseSensitive)) {
          continue;
        }

        try {
          const fileContent = await fs.promises.readFile(fullPath, 'utf8');
          if (regex.test(fileContent)) {
            const stat = await fs.promises.stat(fullPath);
            results.push({
              path: fullPath,
              name: entry.name,
              isDir: false,
              size: stat.size,
              modified: formatDate(stat.mtimeMs / 1000),
              fileType: path.extname(entry.name).slice(1).toUpperCase() || 'FILE'
            });
          }
        } catch {
          // Skip files that can't be read
        }
      }
    }
  }

  await walk(searchDir);
  return results;
}

function sortResults(results: SearchResult[], sortBy: string = 'name'): SearchResult[] {
  return [...results].sort((a, b) => {
    switch (sortBy) {
      case 'name':
        return a.name.toLowerCase().localeCompare(b.name.toLowerCase());
      case 'size':
        return (a.size || 0) - (b.size || 0);
      case 'date':
        return (a.modified || '').localeCompare(b.modified || '');
      case 'type':
        return (a.fileType || '').localeCompare(b.fileType || '');
      default:
        return 0;
    }
  });
}

function formatResults(results: SearchResult[], showDetails: boolean = true): string {
  if (results.length === 0) {
    return '\n' + '='.repeat(70) + '\n  No results found.\n' + '='.repeat(70);
  }

  let output = '\n' + '='.repeat(70) + '\n';
  output += `  Search Results\n  Total: ${results.length} item(s)\n`;
  output += '='.repeat(70) + '\n\n';

  results.forEach((r, i) => {
    const prefix = r.isDir ? '[DIR] ' : '[FILE] ';
    if (showDetails && !r.isDir) {
      const sizeStr = r.size !== undefined ? formatSize(r.size) : 'N/A';
      output += `  [${String(i + 1).padStart(4)}] ${prefix}${r.name.padEnd(35)} ${sizeStr.padStart(12)}  ${r.modified || 'N/A'}  (${r.fileType || '?'})\n`;
      output += `       Path: ${r.path}\n`;
    } else {
      output += `  [${String(i + 1).padStart(4)}] ${prefix}${r.path}\n`;
    }
  });

  output += '\n' + '='.repeat(70) + '\n';
  return output;
}
