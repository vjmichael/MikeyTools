// ===================== FILE CREATION =====================

export interface CreateOptions {
  title?: string;
  headers?: string;
  rows?: string;
  data?: string;
  outputDir?: string;
  styled?: boolean;
  paragraphs?: string;
}

export async function createFile(
  fileType: string,
  filename: string,
  content: string = '',
  options: CreateOptions = {}
): Promise<string> {
  let outputPath: string;

  if (options.outputDir) {
    outputPath = path.join(options.outputDir, filename);
  } else {
    outputPath = path.join(os.homedir(), filename);
  }

  const dir = path.dirname(outputPath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }

  try {
    switch (fileType.toLowerCase()) {
      case 'txt':
        return createTxt(outputPath, content);

      case 'md':
        return createMarkdown(outputPath, content, options);

      case 'json':
        return createJson(outputPath, content);

      case 'csv':
        return createCsv(outputPath, options);

      case 'html':
        return createHtml(outputPath, content, options);

      case 'docx':
        return await createDocx(outputPath, content, options);

      case 'pdf':
        return await createPdf(outputPath, content, options);

      default:
        return `Unknown file type: ${fileType}. Supported: txt, md, json, csv, html, docx, pdf`;
    }
  } catch (e) {
    return `Error creating file: ${e instanceof Error ? e.message : String(e)}`;
  }
}

function createTxt(outputPath: string, content: string): string {
  fs.writeFileSync(outputPath, content, 'utf8');
  return `Created TXT file: ${outputPath} (${content.length} characters)`;
}

function createMarkdown(outputPath: string, content: string, options: CreateOptions): string {
  const title = options.title || 'Document';
  const lines = [`# ${title}`, ''];

  if (content) {
    lines.push(content);
  }

  lines.push('');
  lines.push('---');
  lines.push('*Generated by Universal Toolkit*');

  fs.writeFileSync(outputPath, lines.join('\n'), 'utf8');
  return `Created Markdown file: ${outputPath}`;
}

function createJson(outputPath: string, content: string): string {
  let data: any;

  if (content) {
    try {
      data = JSON.parse(content);
    } catch (e) {
      return `Error: Invalid JSON content.`;
    }
  } else {
    data = {};
  }

  const jsonStr = JSON.stringify(data, null, 2);
  fs.writeFileSync(outputPath, jsonStr, 'utf8');
  return `Created JSON file: ${outputPath} (${jsonStr.length} bytes)`;
}

function parseCsvRow(row: string): string[] {
  const cells: string[] = [];
  let current = '';
  let inQuotes = false;
  for (let i = 0; i < row.length; i++) {
    const char = row[i];
    if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === ',' && !inQuotes) {
      cells.push(current.trim());
      current = '';
    } else {
      current += char;
    }
  }
  cells.push(current.trim());
  return cells;
}

// BUG-42 FIX: Proper CSV parser that handles quoted fields with embedded newlines
function parseCsvContent(content: string): string[][] {
  const rows: string[][] = [];
  let currentRow: string[] = [];
  let currentField = '';
  let inQuotes = false;
  let i = 0;
  
  while (i < content.length) {
    const char = content[i];
    
    if (inQuotes) {
      if (char === '"') {
        // Check for escaped quote (double quote)
        if (i + 1 < content.length && content[i + 1] === '"') {
          currentField += '"';
          i += 2;
        } else {
          // End of quoted field
          inQuotes = false;
          i++;
        }
      } else {
        currentField += char;
        i++;
      }
    } else {
      if (char === '"') {
        inQuotes = true;
        i++;
      } else if (char === ',') {
        currentRow.push(currentField);
        currentField = '';
        i++;
      } else if (char === '\n' || char === '\r') {
        // Handle \r\n and \n line endings
        if (char === '\r' && i + 1 < content.length && content[i + 1] === '\n') {
          i++;
        }
        currentRow.push(currentField);
        if (currentRow.length > 0 || currentField !== '') {
          rows.push(currentRow);
        }
        currentRow = [];
        currentField = '';
        i++;
      } else {
        currentField += char;
        i++;
      }
    }
  }
  
  // Add the last field/row
  if (currentField !== '' || currentRow.length > 0) {
    currentRow.push(currentField);
    rows.push(currentRow);
  }
  
  // Filter out empty rows
  return rows.filter(row => row.length > 0 && (row.length > 1 || row[0].trim() !== ''));
}

function createCsv(outputPath: string, options: CreateOptions): string {
  // BUG-51 FIX: Documented default values - headers default to ['Column1', 'Column2'], rows default to [['val1', 'val2']]
  const headers = options.headers ? options.headers.split(',').map(h => h.trim()) : ['Column1', 'Column2'];
  const rows = options.rows
    ? options.rows.split('|').map(r => parseCsvRow(r))
    : [['val1', 'val2']]; // Default: one row with two columns

  // BUG-10 FIX: csv-stringify handles quoting automatically for values with commas
  // Updated for csv-stringify v6: columns option removed (not needed for array-of-arrays)
  const stringified = csv.stringify([headers, ...rows], {
    header: false,
    delimiter: ',',
    record_delimiter: '\n'
  });

  fs.writeFileSync(outputPath, stringified, 'utf8');
  return `Created CSV file: ${outputPath} (${rows.length} rows, ${headers.length} columns)`;
}

